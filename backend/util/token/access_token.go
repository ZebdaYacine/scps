package util

import (
	"fmt"
	"log"
	"time"

	jwt "github.com/golang-jwt/jwt/v4"
)

type JwtCustomClaims struct {
	ID     string `json:"id"`
	Action string `json:"action"`
	jwt.RegisteredClaims
}

func CreateAccessToken(id string, secret string, expiry int, action string) (accessToken string, err error) {
	exp := time.Now().Add(time.Hour * time.Duration(expiry))
	claims := &JwtCustomClaims{
		ID:     id,
		Action: action,
		RegisteredClaims: jwt.RegisteredClaims{
			ExpiresAt: jwt.NewNumericDate(exp),
		},
	}
	token := jwt.NewWithClaims(jwt.SigningMethodHS256, claims)
	t, err := token.SignedString([]byte(secret))
	if err != nil {
		return "", err
	}
	return t, err
}

func IsAuthorized(requestToken string, secret string) (bool, error) {
	_, err := jwt.Parse(requestToken, func(token *jwt.Token) (interface{}, error) {
		if _, ok := token.Method.(*jwt.SigningMethodHMAC); !ok {
			return nil, fmt.Errorf("unexpected signing method: %v", token.Header["alg"])
		}
		return []byte(secret), nil
	})
	if err != nil {
		return false, err
	}
	return true, nil
}

func ExtractFieldFromToken(requestToken string, secret string, feild string) (any, error) {
	token, err := jwt.Parse(requestToken, func(token *jwt.Token) (interface{}, error) {
		if _, ok := token.Method.(*jwt.SigningMethodHMAC); !ok {
			return nil, fmt.Errorf("unexpected signing method: %v", token.Header["alg"])
		}
		return []byte(secret), nil
	})

	if err != nil {
		return "", err
	}

	claims, ok := token.Claims.(jwt.MapClaims)

	if !ok && !token.Valid {
		return "", fmt.Errorf("invalid Token")
	}
	val, ok := claims[feild]
	log.Println(ok)
	switch val.(type) {
	case string:
		return claims[feild].(string), nil
	case float64:
		return claims[feild].(float64), nil
	case int32:
		return claims[feild].(int32), nil
	default:
		return nil, nil
	}

}

